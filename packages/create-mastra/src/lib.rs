        let repo_root = Path::new("/tmp/mastra-rs");
        let manifest = render_manifest("demo-app", repo_root);

        assert!(manifest.contains("name = \"demo-app\""));
        assert!(manifest.contains("mastra-core = { path = \"/tmp/mastra-rs/packages/core\" }"));
