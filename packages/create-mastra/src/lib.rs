pub fn render_readme(project_name: &str) -> String {
    format!(
        r#"# {project_name}

Manifest-driven Rust starter generated by `create-mastra`.

## Run

```bash
cargo run
```

## What It Does

- boots a real `mastra-core` agent
- enables in-memory message persistence through `mastra-memory`
- initializes tracing via `mastra-loggers`
- stores a loader-friendly project graph in `src/mastra/mastra.json`
- ships one demo memory, tool, agent, and workflow under `src/mastra/`
- keeps agent instructions and example prompt in `src/mastra/`

## Starter Layout

- `src/main.rs`: runnable bootstrap
- `src/mastra/mastra.json`: starter manifest for CLI/project loaders
- `src/mastra/memories/default-memory.json`: default memory node
- `src/mastra/tools/demo-sum.json`: demo tool node
- `src/mastra/agents/demo-agent.json`: demo agent node
- `src/mastra/agents/demo-agent.md`: agent instructions
- `src/mastra/workflows/demo-workflow.json`: demo workflow node
- `src/mastra/resources/hello.txt`: example prompt

Edit `src/main.rs` to replace the echo model with your own agent logic, or grow the manifest/resources layout as your project evolves.
"#
    )
}

