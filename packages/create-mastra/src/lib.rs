pub fn default_repo_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .ancestors()
        .nth(2)
        .expect("workspace root")
        .to_path_buf()
}

pub fn scaffold(path: &Path, repo_root: &Path) -> Result<(), Box<dyn std::error::Error>> {
    let project_name = path
        .file_name()
        .and_then(|name| name.to_str())
        .unwrap_or("mastra-app");
    let src_dir = path.join("src");
    let mastra_dir = src_dir.join("mastra");
    let memory_dir = mastra_dir.join("memories");
    let tool_dir = mastra_dir.join("tools");
    let agent_dir = mastra_dir.join("agents");
    let workflow_dir = mastra_dir.join("workflows");
    let resource_dir = mastra_dir.join("resources");

    fs::create_dir_all(&src_dir)?;
    fs::create_dir_all(&memory_dir)?;
    fs::create_dir_all(&tool_dir)?;
    fs::create_dir_all(&agent_dir)?;
    fs::create_dir_all(&workflow_dir)?;
    fs::create_dir_all(&resource_dir)?;
    fs::write(
        path.join("Cargo.toml"),
        render_manifest(project_name, repo_root),
    )?;
    fs::write(path.join("src/main.rs"), render_main_rs())?;
    fs::write(
        path.join(STARTER_MANIFEST_PATH),
        render_starter_manifest(project_name),
    )?;
    fs::write(path.join(STARTER_MEMORY_PATH), render_memory_manifest())?;
    fs::write(path.join(STARTER_TOOL_PATH), render_tool_manifest())?;
    fs::write(path.join(STARTER_AGENT_SPEC_PATH), render_agent_manifest())?;
    fs::write(
        path.join(STARTER_AGENT_INSTRUCTIONS_PATH),
        render_agent_instructions(),
    )?;
    fs::write(path.join(STARTER_WORKFLOW_PATH), render_workflow_manifest())?;
    fs::write(path.join(STARTER_PROMPT_PATH), render_prompt_example())?;
    fs::write(path.join("README.md"), render_readme(project_name))?;
    fs::write(path.join(".env.example"), render_env_example())?;
    Ok(())
}

